package sec1;

public class Crease1 {

	public static void main(String[] args) {
		//증감 연산 (전위연산 / 후위연산)
		int a=5;
		int b=5;
		System.out.println("++a (전위연산) : "+(++a));
		System.out.println("b++ (후위연산) : "+(b++));
		System.out.println(a);
		System.out.println(b);		
		System.out.println("--b (전위연산) : "+(--b));
		System.out.println("a-- (후위연산) : "+(a--));
		System.out.println(a);
		
		
		
		
		
		
		
		
	}

}
